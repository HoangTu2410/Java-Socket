    public void receivedDataProcessing(ObjectWrapper data){
        if(data.getData().equals("false")) {
            jLabelNotification.setText("Incorrect gmail and/or password!");
        }
        else {
            user = (User) data.getData();
            myControl.setMyUser(user);
            (new ClientMainFrm(myControl)).setVisible(true);
            this.dispose();
        }
    }

