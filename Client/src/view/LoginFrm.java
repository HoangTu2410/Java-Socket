    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
            if(txtEmail.getText().equals("") || txtPassword.getText().equals("")){
                jLabelNotification.setText("You haven't fill email and password!");
                return;
            }
            user.setEmail(txtEmail.getText().trim());
            user.setPassword(txtPassword.getText().trim());
            ObjectWrapper data = new ObjectWrapper(ObjectWrapper.LOGIN_USER,user);
            //sending data
            myControl.sendData(data);
            myControl.getActiveFunction().add(new ObjectWrapper(ObjectWrapper.REPLY_LOGIN_USER, this));
    }//GEN-LAST:event_btnLoginActionPerformed

